"use strict"

var num1 = 10
var num2 = 20

// Mayor que (>)
var mayor = num1 > num2
console.log('Es ' + num1 + ' mayor que ' + num2 + '?:', mayor)

// Menor que (<)
var menor = num1 < num2
console.log('Es ' + num1 + ' menor que ' + num2 + '?:', menor)

// Mayor o igual (>=)
var mayor_igual = num1 >= num2
console.log('Es ' + num1 + ' mayor o igual que ' + num2 + '?:', mayor_igual)

// Menor o igual que (<=)
var menor_igual = num1 <= num2
console.log('Es ' + num1 + ' menor o igual que ' + num2 + '?:', menor_igual)

// Igual que (==)
var iguales = num1 == num2
console.log('Son ' + num1 + ' y ' + num2 + ' iguales?:', iguales)

// Distintos (!=)
var distintos = num1 != num2
console.log('Son distintos ' + num1 + ' y ' + num2 + '?:', distintos)